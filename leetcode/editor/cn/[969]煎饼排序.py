class Solution:
    def pancakeSort(self, A):
        res = []
        n = len(A)
        while n:
            # è¿™é“é¢˜æœ‰ä¸ªæ¡ä»¶æ˜¯  n >= A ä¸­çš„ä»»ä½•ä¸€ä¸ªå…ƒç´ 
            idx = A.index(n)
            res.append(idx + 1)
            A = A[:idx+1][::-1] + A[idx+1:]
            res.append(n)
            A = A[:n][::-1] + A[n:]
            n -= 1

        return res


